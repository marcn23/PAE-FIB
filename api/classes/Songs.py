class Song:
    def __init__(self):
        self.title = None
        self.author = None
        self.subtitle = None
        self.orchestra = None
        self.times = None
    
    def __init__(self, title, author, subtitle, orchestra, times):
        self.title = title
        self.author = author
        self.subtitle = subtitle
        self.orchestra = orchestra
        self.times = times
    